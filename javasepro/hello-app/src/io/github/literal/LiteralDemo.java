package io.github.literal;

public class LiteralDemo {
    public static void main(String[] args) {
//      1.整数
        System.out.println(001);

//      2.小数
        System.out.println(99.8);

//      3.字符，必须‘单引号’围起来，必有且只能有一个字符
        System.out.println('a');
        System.out.println('原');
        System.out.println(' '); //空字符

//      4.字符串，必须“双引号”围起来，内容随意
        System.out.println("");
        System.out.println("dadwa");

//      5.特殊字符
//      true   false
//      null
//      \n--换行   \t--tab
        System.out.println(true);



    }
}
